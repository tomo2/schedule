    {

        Event::create([
            'name' => $request['name'],
            'care' => $request['care'],
            'date' => $request['date'],
            'start_time' => $request['start_time'],
            'end_time' => $request['end_time'],
        ]);

        session()->flash('status', '登録okです');

        return to_route('events.index');
    }

